### Slice
football_club = "Liverpool"
print(football_club[0:4])  # Liver
print(football_club[0:400])  # Liverpool
print(football_club[4:])  # pool
print(football_club[:2])  # Li
print(football_club[0:5:2])  # Lvr # [start:stop:step]

### Escape Characters
print("Hello \nworld")
print("Hello \tworld")
print("Hello \\world")

### Triple Quotes

address = """
123 Main St
City, State, Zip
"""
