### Nested Conditionals

fav_color = "blue"
fav_movie = "The Matrix"
fav_food = "pizza"

if fav_color == "blue":
    print("My favorite color is blue")
    if fav_movie == "The Matrix":
        print("My favorite movie is The Matrix")
        if fav_food == "pizza":
            print("My favorite food is pizza")
